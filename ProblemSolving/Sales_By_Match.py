def sockMerchant(n, ar):
    # Write your code here
    set1  = set(ar)
    pairs = 0
    for i in set1:
        count = ar.count(i)
        if (count % 2 == 1):
            count -= 1
        pairs += count/2
    return int(pairs)
  
